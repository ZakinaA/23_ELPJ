    private void jlbl_ajoutinterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jlbl_ajoutinterActionPerformed
           
            Intervention i = new Intervention ();
            String situationSelected = (String)jlbl_situ.getSelectedItem();
            String idsitu = situationSelected.replaceAll("[^0-9]","");
            int ids;
            ids = Integer.parseInt(idsitu);

            Situation situa = new Situation();
            situa.setSituId(ids);
            
            i.setInterDate(jlbl_date.getText());
            i.setInterDuree(jlbl_duree.getText());
            i.setInterHeureAppel(jlbl_appel.getText());
            i.setInterHeureArrive(jlbl_arriver.getText());
            i.setInterLieu(jlbl_lieu.getText());
            i.setSituation(situa);
            
            DAO.PompierDAO.addIntervention(cnt, i);


    }//GEN-LAST:event_jlbl_ajoutinterActionPerformed

